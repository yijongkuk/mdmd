import type { ZoneType } from './lookupTable';

/**
 * 지자체 조례 기반 건폐율/용적률 오버라이드 테이블
 *
 * 국토계획법 기준(법정 상한)이 아닌 지자체 조례 기준값을 적용합니다.
 * 건폐율·용적률만 오버라이드하며, 높이·이격거리는 법정 기준을 유지합니다.
 *
 * 매칭 우선순위: 시군구 5자리 > 시 4자리 > 시도 2자리
 * → 특정 시군구 데이터가 없으면 상위 시/시도 값으로 자동 폴백
 */

export interface MunicipalOverride {
  maxCoverageRatio: number;
  maxFloorAreaRatio: number;
}

export interface MunicipalityEntry {
  name: string;
  overrides: Partial<Record<ZoneType, MunicipalOverride>>;
}

/** PNU 코드에서 시도(2자리) 추출 */
export function extractSido(pnu: string): string {
  return pnu.slice(0, 2);
}

/** PNU 코드에서 시군구(5자리) 추출 */
export function extractSigungu(pnu: string): string {
  return pnu.slice(0, 5);
}

/** PNU 코드에서 시(4자리) 추출 — 광역시/특별시 외 일반시 매칭용 */
export function extractSi(pnu: string): string {
  return pnu.slice(0, 4);
}

// ─── 헬퍼: 반복되는 오버라이드 패턴을 축약 ─────────────────
// [건폐율, 용적률] 순서
type R = [number, number];

function z(
  r1e: R, r2e: R, r1g: R, r2g: R, r3g: R, rSemi: R,
  cCentral: R, cGeneral: R, cNeighborhood: R, cDistribution: R,
  iSemi: R,
): Partial<Record<ZoneType, MunicipalOverride>> {
  return {
    ZONE_R1_EXCLUSIVE:   { maxCoverageRatio: r1e[0], maxFloorAreaRatio: r1e[1] },
    ZONE_R2_EXCLUSIVE:   { maxCoverageRatio: r2e[0], maxFloorAreaRatio: r2e[1] },
    ZONE_R1_GENERAL:     { maxCoverageRatio: r1g[0], maxFloorAreaRatio: r1g[1] },
    ZONE_R2_GENERAL:     { maxCoverageRatio: r2g[0], maxFloorAreaRatio: r2g[1] },
    ZONE_R3_GENERAL:     { maxCoverageRatio: r3g[0], maxFloorAreaRatio: r3g[1] },
    ZONE_R_SEMI:         { maxCoverageRatio: rSemi[0], maxFloorAreaRatio: rSemi[1] },
    ZONE_C_CENTRAL:      { maxCoverageRatio: cCentral[0], maxFloorAreaRatio: cCentral[1] },
    ZONE_C_GENERAL:      { maxCoverageRatio: cGeneral[0], maxFloorAreaRatio: cGeneral[1] },
    ZONE_C_NEIGHBORHOOD: { maxCoverageRatio: cNeighborhood[0], maxFloorAreaRatio: cNeighborhood[1] },
    ZONE_C_DISTRIBUTION: { maxCoverageRatio: cDistribution[0], maxFloorAreaRatio: cDistribution[1] },
    ZONE_I_SEMI:         { maxCoverageRatio: iSemi[0], maxFloorAreaRatio: iSemi[1] },
  };
}

