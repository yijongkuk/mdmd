/**
 * PNU와 용도지역으로 지자체 조례 오버라이드를 조회합니다.
 * 매칭 우선순위: 시군구 5자리 > 시 4자리 > 시도 2자리
 * 해당 지자체/용도지역에 오버라이드가 없으면 null 반환.
 */
export function getMunicipalOverride(
  pnu: string,
  zoneType: ZoneType,
): MunicipalLookupResult | null {
  if (!pnu || pnu.length < 2) return null;

  // 우선순위 순서: 시군구(5) > 시(4) > 시도(2)
  const keys = [
    pnu.length >= 5 ? extractSigungu(pnu) : null,
    pnu.length >= 4 ? extractSi(pnu) : null,
    extractSido(pnu),
  ];

  for (const key of keys) {
    if (!key) continue;
    const entry = MUNICIPALITY_TABLE[key];
    if (!entry) continue;
    const override = entry.overrides[zoneType];
    if (override) {
      return { municipalityName: entry.name, override };
    }
  }

  return null;
}
